#!/usr/bin/env node
/* eslint-disable no-console */
// Writes a small src/buildMeta.js with the current build date and the
// last commit hash + date for src/data/drugs.js. Runs as a prebuild step
// so the footer can show "Last updated" without a network call.
import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const repoRoot = path.resolve(__dirname, '..');
const target = path.join(repoRoot, 'src', 'buildMeta.js');

const safeExec = (cmd) => {
  try {
    return execSync(cmd, { cwd: repoRoot, stdio: ['ignore', 'pipe', 'ignore'] })
      .toString()
      .trim();
  } catch {
    return '';
  }
};

const buildDate = new Date().toISOString();
const dataLastUpdated =
  safeExec('git log -1 --format=%cI -- src/data/drugs.js src/data/references.js src/data/ctProtocols.json') ||
  buildDate;
const dataCommitHash = safeExec(
  'git log -1 --format=%h -- src/data/drugs.js src/data/references.js src/data/ctProtocols.json'
);

const contents = `// Auto-generated by scripts/generate-build-meta.js — do not edit by hand.
export const BUILD_DATE = ${JSON.stringify(buildDate)};
export const DATA_LAST_UPDATED = ${JSON.stringify(dataLastUpdated)};
export const DATA_COMMIT_HASH = ${JSON.stringify(dataCommitHash)};
`;

fs.mkdirSync(path.dirname(target), { recursive: true });
fs.writeFileSync(target, contents);
console.log(`build-meta written: BUILD_DATE=${buildDate}, DATA_LAST_UPDATED=${dataLastUpdated}`);
